            public void listarClientes() {
    try {
        Connection conn = DriverManager.getConnection(
                "jdbc:mysql://localhost:3306/bibliotecaProjeto", "root", "gaby2008");

        String sql = "SELECT * FROM clientes";
        PreparedStatement pst = conn.prepareStatement(sql);
        ResultSet rs = pst.executeQuery();

        DefaultTableModel model = (DefaultTableModel) tbCliente.getModel();
        model.setRowCount(0); 

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getInt("id_cliente"),
                rs.getString("nome"),
                rs.getString("cpf"),
                rs.getString("telefone"),
                rs.getString("email"),
                rs.getString("endereco")
            });
        }

        rs.close();
        pst.close();
        conn.close();

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Erro ao listar clientes: " + e.getMessage());
    }
}

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        try {
           
            String nome = txtNome.getText().trim();
            if (nome.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Preencha o campo Nome");
                txtNome.requestFocus();
                return;
            }

            String cpf = txtCpf.getText().trim();
            if (cpf.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Preencha o campo CPF");
                txtCpf.requestFocus();
                return;
            }
          
            cpf = cpf.replaceAll("\\D", "");

            String telefone = txtTelefone.getText().trim();
            if (telefone.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Preencha o campo Telefone");
                txtTelefone.requestFocus();
                return;
            }

            String email = txtEmaill.getText().trim();
            if (email.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Preencha o campo Email");
                txtEmail.requestFocus();
                return;
            }

            String endereco = txtEndereco.getText().trim();
            if (endereco.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Preencha o campo Endereço");
                txtEndereco.requestFocus();
                return;
            }

         Connection conn = DriverManager.getConnection(
                    "jdbc:mysql://localhost:3306/bibliotecaProjeto", "root", "gaby2008");

         
            String sql = "INSERT INTO clientes (nome, cpf, telefone, email, endereco) VALUES (?, ?, ?, ?, ?)";
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, nome);
            pst.setString(2, cpf);
            pst.setString(3, telefone);
            pst.setString(4, email);
            pst.setString(5, endereco);

            pst.executeUpdate();

           
            JOptionPane.showMessageDialog(this,
                    "Cliente cadastrado com sucesso!\n\n"
                    + "Nome: " + nome + "\n"
                    + "CPF: " + cpf + "\n"
                    + "Telefone: " + telefone + "\n"
                    + "Email: " + email + "\n"
                    + "Endereço: " + endereco
            );

            
            txtId_Cliente.setText("");
            txtNome.setText("");
            txtCpf.setText("");
            txtTelefone.setText("");
            txtEmaill.setText("");
            txtEndereco.setText("");

          
            DefaultTableModel model = (DefaultTableModel) TelaCliente.tbCliente.getModel();
            model.setRowCount(0);

            String sqlSelect = "SELECT * FROM clientes";
            PreparedStatement pstSelect = conn.prepareStatement(sqlSelect);
            ResultSet rs = pstSelect.executeQuery();
            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getInt("id_cliente"),
                    rs.getString("nome"),
                    rs.getString("cpf"),
                    rs.getString("telefone"),
                    rs.getString("email"),
                    rs.getString("endereco")
                });
            }

           
            rs.close();
            pstSelect.close();
            pst.close();
            conn.close();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao salvar cliente: " + e.getMessage());
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

