    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed

    try {
        String nomePesquisa = txtNome.getText().trim();

        if (nomePesquisa.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Digite um nome para pesquisar!");
            return;
        }

        // Conexão com o banco de dados
        Connection conn = DriverManager.getConnection(
                "jdbc:mysql://localhost:3306/bibliotecaProjeto", "root", "gaby2008");

        String sql = "SELECT * FROM clientes WHERE nome LIKE ?";
        PreparedStatement pst = conn.prepareStatement(sql);
        pst.setString(1, "%" + nomePesquisa + "%");

        ResultSet rs = pst.executeQuery();

        // Modelo da tabela
        DefaultTableModel model = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false; 
            }
        };

        // Definindo colunas
        model.addColumn("ID");
        model.addColumn("Nome");
        model.addColumn("CPF");
        model.addColumn("Telefone");
        model.addColumn("Email");
        model.addColumn("Endereço");

        boolean encontrou = false;

        while (rs.next()) {
            encontrou = true;

            Object[] row = new Object[6];
            row[0] = rs.getInt("id_cliente");
            row[1] = rs.getString("nome");
            row[2] = rs.getString("cpf");
            row[3] = rs.getString("telefone");
            row[4] = rs.getString("email");
            row[5] = rs.getString("endereco");

            model.addRow(row);
        }

        if (encontrou) {
            tbCliente.setModel(model); // <<-- usa a sua JTable
        } else {
            JOptionPane.showMessageDialog(this, "Cliente não encontrado!");
            tbCliente.setModel(new DefaultTableModel()); // Limpa a tabela
        }

        rs.close();
        pst.close();
        conn.close();

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Erro de banco: " + e.getMessage());
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Erro: " + e.getMessage());
    }

    }//GEN-LAST:event_btnPesquisarActionPerformed

